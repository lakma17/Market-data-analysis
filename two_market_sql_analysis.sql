--10. Which ad channel has the highest ROI(avg spending per responder)?
with customer_avg_spending as (
    select
        m.id,
        m.response,
        (m.amtliq + m.amtvege + m.amtnonveg + 
         m.amtpes + m.amtchocolates + m.amtcomm) AS total_revenue,
        a.bulkmail_ad,
        a.twitter_ad,
        a.instagram_ad,
        a.facebook_ad,
        a.brochure_ad
    from marketing_data m
    join ad_data a on m.id = a.id
),

channel_avg as (
    select 'Bulk Mail' as channel, avg(case when bulkmail_ad = 1 and response = 1 then total_revenue end) as avg_spending
    from customer_avg_spending
    union all
    select 'Twitter', avg(case when twitter_ad = 1 and response = 1 then total_revenue end)
    from customer_avg_spending
    union all
    select 'Instagram', avg(case when instagram_ad = 1 and response = 1 then total_revenue end)
    from customer_avg_spending
    union all
    select 'Facebook', avg(case when facebook_ad = 1 and response = 1 then total_revenue end)
    FROM customer_avg_spending
    UNION ALL
    select 'Brochure', avg(case when brochure_ad = 1 and response = 1 then total_revenue end)
    from customer_avg_spending
),

channel_rank as (
 
    select 
        channel,
        avg_spending,
        rank() over (order by avg_spending DESC) as roi_rank
    FROM channel_avg
)
select *
from channel_rank
order by roi_rank;


