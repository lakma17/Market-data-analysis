----22. Classify engagement level:
--* 0 ads → No Engagement
--* 1–2 ads → Low
--* 3+ ads → High

with customer_response as(select
    m.id,
    m.response,
    (a.bulkmail_ad + a.twitter_ad + a.instagram_ad + a.facebook_ad + a.brochure_ad + m.response) as total_ad_responded

from marketing_data m
join ad_data a on m.id = a.id),

	customer_engagement as(
		select 
			count(case when total_ad_responded =0 then 1 end) as no_engagemnt,
			count(case when total_ad_responded between 1 and 2 then 1 end) as low_engagement,
			count(case when total_ad_responded>= 3 then 1 end) as high_engagment
	from customer_response
	)	
select * from customer_engagement;
		

---23. Do high-engagement customers spend more?

with customer_response as(select
    m.response,  m."id",
    (a.bulkmail_ad + a.twitter_ad + a.instagram_ad + a.facebook_ad + a.brochure_ad + m.response) as total_ad_responded,
	sum(m.amtliq + m.amtvege + m.amtnonveg + m.amtpes + m.amtchocolates + m.amtcomm) as total_spent
from marketing_data m
join ad_data a on m.id = a.id

group by m.id, m.response,
             a.bulkmail_ad, a.twitter_ad, a.instagram_ad, a.facebook_ad, a.brochure_ad),
			 
	customer_engagement as(
		select 
			"id",
			total_spent,
			case 
				when total_ad_responded =0 then 'no_engagemnt'
				when total_ad_responded between 1 and 2 then 'low_engagement'
				when total_ad_responded>= 3 then 'high_engagement' end as customer_engagement_level
	from customer_response
	)
select
    customer_engagement_level,
    count(*) as num_customers,
    avg(total_spent) as avg_spent,
    sum(total_spent) as total_spent
from customer_engagement
group by customer_engagement_level
order by avg_spent DESC;

