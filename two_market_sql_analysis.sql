------------------------ Customer & Revenue Overview

--1. What is the total revenue generated by all customers?
select  sum(amtliq + amtvege + amtnonveg + 
         amtpes + amtchocolates + amtcomm) AS total_revenue

from marketing_data;



--2. What is the average spending per customer?
select round(avg(amtliq + amtvege + amtnonveg + 
         amtpes + amtchocolates + amtcomm),2) AS avg_revenue_per_customer

from marketing_data;

--3. Who are the top 10% highest spenders?
with highest_spenders as(
		select "id", sum(amtliq + amtvege +amtnonveg+amtpes+ amtchocolates +amtcomm) as customer_spent
		from marketing_data
		group by "id") ,

	rank_top_ten_pct as(
		select "id", customer_spent, Ntile(10) over(order by customer_spent DESC) as top_tier
		from highest_spenders)

select "id", customer_spent
from rank_top_ten_pct
where top_tier=1;

--4. Which country generates the highest total revenue?
select country, sum(amtliq + amtvege +amtnonveg+amtpes+ amtchocolates +amtcomm) as country_total
from marketing_data
group by country
order by country_total DESC
limit 1;
		 
--5. What is the revenue contribution (%) by each country?	
with country_rev as(
	select 
		country, 
		sum(amtliq + amtvege +amtnonveg+amtpes+ amtchocolates +amtcomm) as Total_rev
	from marketing_data
	group by country),

	rev_pct as(
		select country, 
			round(Total_rev *100/ sum(Total_rev) over(),2) as country_rev_pct
		from country_rev)
select country, 
		country_rev_pct
		from rev_pct
		ORDER BY country_rev_pct DESC;

		
