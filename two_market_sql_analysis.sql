----------------------- Customer Segmentation

--15. Which age group responds most to social media ads?

select 
	case
		when extract(year from current_date) - year_birth between 29 and 39 then '29-39'
		when extract(year from current_date) -year_birth between 40 and 50 then '40-50'
		when extract(year from current_date)- year_birth between 51 and 61 then '51-61'
		when extract(year from current_date)- year_birth between 62 and 72 then '62-72'
		when extract(year from current_date) -year_birth between 73 and 132 then 'senior' end as age_group,
		
	count(*) as total_responses
from marketing_data m
join ad_data a 
on m.id = a.id
where m.response = 1
group by age_group
order by total_responses DESC;



---16. Do younger customers prefer Instagram?

select
	case when extract(year from current_date)- year_birth <35 then 'younger_customer' else  'older_customers' end as age_groups,
	sum(a.instagram_ad) as instgram_responses
	from marketing_data m
	join ad_data a on m.id = a.id
	group by age_groups;

---17. Are seniors more responsive to brochure ads?

select 
	case when extract(year from current_date)-year_birth >=60 then 'seniors' else 'non_seniors' end as age_groups,
	sum(a.brochure_ad) as brochure_responses
	from marketing_data m
	join ad_data a on m.id=a.id
	group by age_groups;
	
----18. Which education level has highest engagement rate?
select education, 
	count(*) as total_customers,
	sum(a.bulkmail_ad + a.twitter_ad + a.instagram_ad +
    a.facebook_ad + a.brochure_ad) as total_engagement,
	
	round(sum(a.bulkmail_ad + a.twitter_ad + a.instagram_ad +
        a.facebook_ad + a.brochure_ad)::numeric / count(*), 2) as engagement_rate
from marketing_data m
join ad_data a on m.id =a.id
group by education
order by engagement_rate DESC;

