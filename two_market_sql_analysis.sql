--8. Which ad channel generated the highest total revenue?

with total_revenue as(
 	select m."id", m.response,
 		(amtliq + amtvege +amtnonveg+amtpes+ amtchocolates +amtcomm) as total_rev
	from marketing_data	m
),
ad_rev as(
	select 
		round(sum(case when t.response = 1 and a.bulkmail_ad =1 then total_rev else 0 end),2) as bulkmail_rev,
		round(sum(case when t.response =1 and a.twitter_ad =1 then total_rev else 0 end), 2) as twitter_rev,
		round(sum(case when t.response =1 and  a.instagram_ad =1 then total_rev else 0 end),2) as instagram_rev,	
		round(sum(case when t.response =1 and a.facebook_ad =1 then total_rev else 0 end),2) as facebook_rev,	
