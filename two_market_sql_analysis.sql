    (a.bulkmail_ad + a.twitter_ad + a.instagram_ad + a.facebook_ad + a.brochure_ad + m.response) as total_ad_responded,
	(m.amtliq + m.amtvege + m.amtnonveg + m.amtpes + m.amtchocolates + m.amtcomm) as total_spent
from marketing_data m
join ad_data a on m.id = a.id
),
	ad_engagement as(select
		"id",
		total_spent,
		case
			when total_ad_responded =5 then 'responded_to_all_ads' else 'responded_non_or_more_than_one' end as engagement_level
		from customer_response
	)
	
select
	engagement_level,
	count(*) as num_customers,
	avg(total_spent) as avg_spent,
	sum(total_spent) as total_spent
from ad_engagement
group by engagement_level
order by num_customers
	
